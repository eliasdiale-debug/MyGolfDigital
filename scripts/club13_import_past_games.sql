-- Step 4: Build game_id map using the inserted adhoc_games
DROP TABLE IF EXISTS _wsoe_game_ids;
CREATE TEMP TABLE _wsoe_game_ids AS
SELECT
  ROW_NUMBER() OVER (ORDER BY game_date) AS game_num,
  adhoc_game_id,
  game_date,
  course_id,
  CASE
    WHEN game_date IN ('2026-02-04','2026-02-11') THEN 71
    ELSE 72
  END AS par
FROM adhoc_games
WHERE club_id = 13 AND status = 'completed'
  AND game_date BETWEEN '2026-01-14' AND '2026-03-11';

-- Step 5: Insert into performance_records for all rows
-- Only insert where a row exists in _wsoe_raw
-- gross = par + playing_handicap - (COALESCE(points,36) - 36)
-- If no score, we still log the playing handicap for record-keeping (points=0, gross=par+phcp)
INSERT INTO performance_records (member_id, course_id, game_date, points, gross_score, club_id)
SELECT
  r.member_id,
  g.course_id,
  g.game_date,
  COALESCE(r.points, 0) AS points,
  CASE
    WHEN r.points IS NOT NULL AND r.points > 0
    THEN g.par + r.playing_handicap - (r.points - 36)
    ELSE NULL
  END AS gross_score,
  13 AS club_id
FROM _wsoe_raw r
JOIN _wsoe_game_ids g ON g.game_num = r.game_num
ON CONFLICT DO NOTHING;

-- Step 6: Insert into pairings (one per member per game, fourball=1 for historical)
INSERT INTO pairings (adhoc_game_id, fourball_number, member_id, is_captain, gross_score, points, result_submitted, playing_handicap, birdies_count, eagles_count, hio_count, ladies_count, is_late, is_no_show)
SELECT
  g.adhoc_game_id,
  1 AS fourball_number,
  r.member_id,
  FALSE AS is_captain,
  CASE
    WHEN r.points IS NOT NULL AND r.points > 0
    THEN g.par + r.playing_handicap - (r.points - 36)
    ELSE NULL
  END AS gross_score,
  CASE WHEN r.points IS NOT NULL AND r.points > 0 THEN r.points ELSE NULL END AS points,
  CASE WHEN r.points IS NOT NULL AND r.points > 0 THEN TRUE ELSE FALSE END AS result_submitted,
  r.playing_handicap,
  0, 0, 0, 0, FALSE, FALSE
FROM _wsoe_raw r
JOIN _wsoe_game_ids g ON g.game_num = r.game_num
ON CONFLICT DO NOTHING;

-- Step 7: Verification
SELECT
  g.game_date,
  c.course_name,
  COUNT(*) AS total_records,
  COUNT(pr.gross_score) AS with_scores,
  ROUND(AVG(pr.points) FILTER (WHERE pr.points > 0), 1) AS avg_points
FROM performance_records pr
JOIN courses c ON pr.course_id = c.course_id
JOIN adhoc_games g ON g.course_id = pr.course_id AND g.game_date = pr.game_date AND g.club_id = 13
WHERE pr.club_id = 13 AND pr.game_date BETWEEN '2026-01-14' AND '2026-03-11'
GROUP BY g.game_date, c.course_name
ORDER BY g.game_date;
